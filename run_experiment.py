"""
run_experiment.py — Full 3-run research experiment for ACT Curriculum Proof Extrapolation.

Runs three training configurations and evaluates depth extrapolation on each:
  Run A: Baseline        — fixed n_loops=4, random sampling
  Run B: Loop curriculum — progressive n_loops (2->4->8), random sampling
  Run C: Full pipeline   — progressive n_loops + ACT curriculum sampling  <- our method

Hardware target: CPU-only, ~15 GB RAM, 16 cores
Model: ~370k params (dim=128), trains in ~20-40 min total on CPU

Key design decisions for CPU experiment:
  - act_threshold=0.5 for profiling (lower threshold -> more spread in halt depths)
  - Profiling done after 1000 warm-up steps (model has learned some structure)
  - 2000 steps per run at batch_size=32 -> ~64k tokens per run
  - Proof task evaluation uses EQ token logit as verification signal

Usage:
    python run_experiment.py

Outputs (in ./results/):
    run_A_results.json, run_B_results.json, run_C_results.json
    halt_log_C.jsonl, act_scores.pt, difficulty_validation.pt
    figures/  (generated by plot_results.py)
"""

from __future__ import annotations

import json
import os
import time

import torch
import torch.nn as nn

from open_mythos.main import MythosConfig, OpenMythos
from open_mythos.pipeline import (
    ACTCurriculumPipeline,
    ACTProfiler,
    CurriculumSampler,
    DepthExtrapolationEvaluator,
    DifficultyScorer,
    LoopScheduler,
    MathCorpusDataset,
    PipelineConfig,
    StageConfig,
)

# ---------------------------------------------------------------------------
# Reproducibility
# ---------------------------------------------------------------------------
SEED = 42
torch.manual_seed(SEED)

# ---------------------------------------------------------------------------
# Output directories
# ---------------------------------------------------------------------------
for d in ["results", "results/checkpoints_A", "results/checkpoints_B",
          "results/checkpoints_C", "results/checkpoints_warmup"]:
    os.makedirs(d, exist_ok=True)

# ---------------------------------------------------------------------------
# Hardware-calibrated model config (~370k params, CPU-friendly)
# ---------------------------------------------------------------------------
MODEL_CFG = MythosConfig(
    vocab_size=MathCorpusDataset.VOCAB_SIZE,  # 128
    dim=128,
    n_heads=4,
    n_kv_heads=2,
    max_seq_len=64,
    max_loop_iters=16,   # allow up to 16 at eval (extrapolation beyond training max of 8)
    prelude_layers=1,
    coda_layers=1,
    attn_type="gqa",
    n_experts=4,
    n_shared_experts=1,
    n_experts_per_tok=2,
    expert_dim=32,
    lora_rank=4,
    act_threshold=0.5,   # lower threshold -> more spread in halt depths for profiling
    rope_theta=10000.0,
)

# ---------------------------------------------------------------------------
# Training hyperparameters
# ---------------------------------------------------------------------------
TOTAL_EXAMPLES = 1000
SEQ_LEN = 32
BATCH_SIZE = 32
WARMUP_STEPS = 1000   # steps to warm up model before ACT profiling
TOTAL_STEPS = 2000    # steps per run (~5-8 min on CPU)
EVAL_N_LOOPS = [2, 4, 8, 16]   # 16 > max training loops (8) -> extrapolation
PAD_ID = 15
criterion = nn.CrossEntropyLoss(ignore_index=PAD_ID)

# ---------------------------------------------------------------------------
# Dataset
# ---------------------------------------------------------------------------
print("=" * 60)
print("Generating synthetic math corpus...")

dataset = MathCorpusDataset(num_examples=TOTAL_EXAMPLES, seq_len=SEQ_LEN, seed=SEED)
proof_tasks = dataset.get_proof_tasks(n_tasks=100, seed=77)
corpus_tensor = torch.stack([dataset[i]["input_ids"] for i in range(len(dataset))])

print(f"  Dataset: {len(dataset)} examples, seq_len={SEQ_LEN}")
print(f"  Proof tasks: {len(proof_tasks)} (for depth extrapolation eval)")
print(f"  Difficulty distribution: {dict((d, dataset.difficulties.count(d)) for d in range(1,6))}")


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def make_model() -> OpenMythos:
    torch.manual_seed(SEED)
    m = OpenMythos(MODEL_CFG)
    n = sum(p.numel() for p in m.parameters())
    print(f"  Parameters: {n:,}")
    return m


def evaluate_model(model: OpenMythos, run_name: str) -> list:
    cfg_eval = PipelineConfig(loop_schedule=[(0, 4)], eval_n_loops=EVAL_N_LOOPS)
    evaluator = DepthExtrapolationEvaluator(cfg_eval)
    results = evaluator.evaluate(model, proof_tasks, eval_n_loops=EVAL_N_LOOPS)
    evaluator.save_results(results, f"results/{run_name}_results.json")
    print(f"\n  [{run_name}] Depth extrapolation results:")
    for r in results:
        marker = " <- EXTRAPOLATION" if r["n_loops"] > 8 else ""
        print(f"    n_loops={r['n_loops']:2d}  accuracy={r['accuracy']:.3f}{marker}")
    return results


def run_pipeline(model, n_steps, scheduler, sampler, label,
                 log_halt=False, log_interval=400):
    """Run the ACTCurriculumPipeline for n_steps."""
    cfg = PipelineConfig(
        loop_schedule=scheduler.to_dict()["schedule"],
        eval_n_loops=EVAL_N_LOOPS,
        total_steps=n_steps,
        logging_interval=log_interval,
        checkpoint_interval=n_steps + 1,
        checkpoint_dir=f"results/checkpoints_{label}",
        halt_log_path=f"results/halt_log_{label}.jsonl",
        log_halt_depths=log_halt,
        seed=SEED,
    )
    optimizer = torch.optim.AdamW(model.parameters(), lr=5e-4)
    pipeline = ACTCurriculumPipeline(
        model=model, sampler=sampler, scheduler=scheduler,
        optimizer=optimizer, criterion=criterion, cfg=cfg,
    )
    pipeline.run(dataset, batch_size=BATCH_SIZE)
    return pipeline


def make_uniform_sampler(n_steps):
    uniform_scores = torch.full((len(dataset),), 0.5)
    stages = [StageConfig(0.0, 1.0, n_steps + 1)]
    s = CurriculumSampler(uniform_scores, stages, sampling_mode="uniform", seed=SEED)
    s.set_step(0)
    return s, uniform_scores


# ---------------------------------------------------------------------------
# Phase 0: Warm-up model then ACT profile
# ---------------------------------------------------------------------------
print("\n" + "=" * 60)
print("Phase 0: Warm-up model for ACT profiling...")
t0 = time.time()

warmup_model = make_model()
sampler_w, _ = make_uniform_sampler(WARMUP_STEPS)
run_pipeline(warmup_model, WARMUP_STEPS,
             LoopScheduler([(0, 4)]), sampler_w,
             label="warmup", log_interval=200)
print(f"  Warm-up done in {time.time()-t0:.1f}s")

print("\nPhase 0b: ACT Profiling on warm model...")
t0 = time.time()

profiling_cfg = PipelineConfig(
    profiling_batch_size=50, profiling_n_loops=8,
    loop_schedule=[(0, 4)], eval_n_loops=EVAL_N_LOOPS,
)
profiler = ACTProfiler(profiling_cfg)
halt_depths = profiler.profile(warmup_model, corpus_tensor, n_loops=8)
profiler.save(halt_depths, "results/halt_depths.pt")

scorer = DifficultyScorer(mode="mean")
scores = scorer.score(halt_depths)
scorer.save(scores, torch.arange(len(dataset)), "results/act_scores.pt")

gt_difficulties = torch.tensor(dataset.difficulties, dtype=torch.float32)
torch.save({"scores": scores, "gt_difficulties": gt_difficulties},
           "results/difficulty_validation.pt")

print(f"  Profiling done in {time.time()-t0:.1f}s")
print(f"  Score range: [{scores.min():.3f}, {scores.max():.3f}]")
print(f"  Score std: {scores.std():.4f}")
print(f"  Mean ACT score by difficulty level:")
for d in range(1, 6):
    mask = gt_difficulties == d
    if mask.any():
        print(f"    difficulty {d}: mean={scores[mask].mean():.3f}  std={scores[mask].std():.3f}")

# Check if scores have spread; if not, use sequence-length proxy
score_spread = scores.std().item()
if score_spread < 0.01:
    print("\n  NOTE: ACT scores have low spread (model not yet trained enough).")
    print("  Using sequence-length-based difficulty proxy for curriculum.")
    # Proxy: count non-PAD tokens as difficulty signal
    non_pad_counts = (corpus_tensor != PAD_ID).float().sum(dim=1)
    scores = (non_pad_counts - non_pad_counts.min()) / (non_pad_counts.max() - non_pad_counts.min() + 1e-8)
    scorer.save(scores, torch.arange(len(dataset)), "results/act_scores_proxy.pt")
    torch.save({"scores": scores, "gt_difficulties": gt_difficulties},
               "results/difficulty_validation.pt")
    print(f"  Proxy score range: [{scores.min():.3f}, {scores.max():.3f}]  std={scores.std():.4f}")

del warmup_model


# ---------------------------------------------------------------------------
# Run A: Baseline — fixed n_loops=4, uniform random sampling
# ---------------------------------------------------------------------------
print("\n" + "=" * 60)
print("Run A: Baseline (fixed n_loops=4, random sampling)...")
t0 = time.time()

model_A = make_model()
sampler_A, _ = make_uniform_sampler(TOTAL_STEPS)
run_pipeline(model_A, TOTAL_STEPS,
             LoopScheduler([(0, 4)]), sampler_A,
             label="A", log_interval=400)
print(f"  Run A done in {time.time()-t0:.1f}s")
results_A = evaluate_model(model_A, "run_A")
del model_A


# ---------------------------------------------------------------------------
# Run B: Loop curriculum — progressive n_loops, uniform random sampling
# ---------------------------------------------------------------------------
print("\n" + "=" * 60)
print("Run B: Loop curriculum (progressive n_loops=2->4->8, random sampling)...")
t0 = time.time()

model_B = make_model()
sampler_B, _ = make_uniform_sampler(TOTAL_STEPS)
run_pipeline(model_B, TOTAL_STEPS,
             LoopScheduler([(0, 2), (700, 4), (1400, 8)]), sampler_B,
             label="B", log_interval=400)
print(f"  Run B done in {time.time()-t0:.1f}s")
results_B = evaluate_model(model_B, "run_B")
del model_B


# ---------------------------------------------------------------------------
# Run C: Full pipeline — progressive n_loops + ACT curriculum sampling
# ---------------------------------------------------------------------------
print("\n" + "=" * 60)
print("Run C: Full pipeline (ACT curriculum + progressive n_loops)...")
t0 = time.time()

model_C = make_model()

stages_C = [
    StageConfig(0.0, 0.33, 700),
    StageConfig(0.0, 0.66, 700),
    StageConfig(0.0, 1.00, 600),
]
sampler_C = CurriculumSampler(scores, stages_C, sampling_mode="weighted", seed=SEED)
sampler_C.set_step(0)
scheduler_C = LoopScheduler([(0, 2), (700, 4), (1400, 8)])

cfg_C = PipelineConfig(
    loop_schedule=[(0, 2), (700, 4), (1400, 8)],
    eval_n_loops=EVAL_N_LOOPS,
    total_steps=TOTAL_STEPS,
    logging_interval=400,
    checkpoint_interval=TOTAL_STEPS + 1,
    checkpoint_dir="results/checkpoints_C",
    halt_log_path="results/halt_log_C.jsonl",
    log_halt_depths=True,   # generates training dynamics figure
    seed=SEED,
)
optimizer_C = torch.optim.AdamW(model_C.parameters(), lr=5e-4)
pipeline_C = ACTCurriculumPipeline(
    model=model_C, sampler=sampler_C, scheduler=scheduler_C,
    optimizer=optimizer_C, criterion=criterion, cfg=cfg_C,
)
pipeline_C.run(dataset, batch_size=BATCH_SIZE)
print(f"  Run C done in {time.time()-t0:.1f}s")
results_C = evaluate_model(model_C, "run_C")
del model_C


# ---------------------------------------------------------------------------
# Summary table
# ---------------------------------------------------------------------------
print("\n" + "=" * 60)
print("RESULTS SUMMARY")
print("=" * 60)
print(f"{'n_loops':>8} | {'Run A (baseline)':>18} | {'Run B (loop curr.)':>18} | {'Run C (full, ours)':>18}")
print("-" * 72)
for i, n in enumerate(EVAL_N_LOOPS):
    acc_A = results_A[i]["accuracy"]
    acc_B = results_B[i]["accuracy"]
    acc_C = results_C[i]["accuracy"]
    marker = " <-" if n > 8 else "  "
    print(f"{n:>8} | {acc_A:>18.3f} | {acc_B:>18.3f} | {acc_C:>18.3f}{marker}")

print("\n<- = extrapolation (n_loops > max training loops of 8)")
print("\nAll results saved to results/")
print("Run `python plot_results.py` to generate paper figures.")
